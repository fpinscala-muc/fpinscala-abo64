    val genZero = Gen.union(gen, Gen.unit(zero)) // zero as interesting corner case
    // Associativity
    forAll(for {
      x <- genZero
      y <- genZero
      z <- genZero
    } yield (x, y, z)) { case (x, y, z) =>
      op(x, op(y, z)) == op(op(x, y), z) } &&
    // Identity
    forAll(gen)((a: A) =>
      op(a, zero) == a && op(zero, a) == a)
  }

